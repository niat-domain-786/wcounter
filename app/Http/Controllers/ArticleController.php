    public function html_to_text(Request $request)
    {
        // return response()->json($request->ed_data);

        $options = array(
          'ignore_errors' => true,
          'drop_links'=>true
        );


        $html = $request->ed_data;
            

        $text = \Soundasleep\Html2Text::convert($html, $options);

       return [$text];
    }


